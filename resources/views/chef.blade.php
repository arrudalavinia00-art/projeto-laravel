  <h1>Receita de Brigadeiro Perfeito</h1>
  <p>O brigadeiro é o doce perfeito para qualquer ocasião.</p>
  <h2>Ingredientes</h2>
  <ul>
    <li>1 lata de leite condensado</li>
    <li>2 colheres (sopa) de chocolate em pó</li>
    <li>1 colher (sopa) de manteiga</li>
    <li>Chocolate granulado para enrolar</li>
  </ul>
  <h2>Modo de Preparo</h2>
  <ol>
    <li>Em uma panela, coloque o leite condensado, o chocolate em pó e a manteiga.</li>
    <li>Cozinhe em fogo baixo, mexendo sempre para não grudar.</li>
    <li>Continue mexendo até desgrudar do fundo da panela (ponto de brigadeiro).</li>
    <li>Deixe esfriar, enrole as bolinhas e passe no granulado.</li>
    <li>Pronto! Sirva em forminhas e aproveite.</li>
  </ol>
